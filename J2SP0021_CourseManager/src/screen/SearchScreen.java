    public boolean validateCode(String code){
        String codeRegex = "^[a-zA-Z0-9\\_]{3,10}";
        if(!code.matches(codeRegex)){
            JOptionPane.showMessageDialog(this, "Invalied code");
            return false;
        }
        return true;
    }
   
