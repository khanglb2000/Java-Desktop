    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String code = txtCode.getText();
        txtCredit.setText("");
        txtName.setText("");
        boolean found = false;
        if(validateCode(code) == false){
            return;
        }
        try {
            Course x = service.getDataByCode(code);
            if(x != null){
                txtName.setText(x.getName());
                txtCredit.setText(Integer.toString(x.getCredit()));
            }else{
                JOptionPane.showMessageDialog(this, "Course does not exit");
                txtCode.setText("");
            }            
        } catch (Exception e) {
        }

    }//GEN-LAST:event_btnSearchActionPerformed

