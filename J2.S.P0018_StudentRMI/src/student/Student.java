package student;

/**
 *
 * @author HI
 */
public class Student {
    String email, name;
    int age;
    boolean sex;

    public Student(String email, String name, int age, boolean sex) {
        this.email = email;
        this.name = name;
        this.age = age;
        this.sex = sex;
    }

    public Student() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isSex() {
        return sex;
    }

    public void setSex(boolean sex) {
        this.sex = sex;
    }
    
}
